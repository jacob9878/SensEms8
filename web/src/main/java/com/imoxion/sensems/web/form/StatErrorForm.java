package com.imoxion.sensems.web.form;

public class StatErrorForm {
    private String cpage = "1";

    private String pagegroupsize = "5";

    private String srch_type;

    private String srch_keyword;

    private String msgid;

    private String errcode;

    private String err_exp;

    private String field1;

    public String getCpage() { return cpage; }

    public void setCpage(String cpage) { this.cpage = cpage; }

    public String getPagegroupsize() { return pagegroupsize; }

    public void setPagegroupsize(String pagegroupsize) { this.pagegroupsize = pagegroupsize; }

    public String getSrch_type() { return srch_type; }

    public void setSrch_type(String srch_type) { this.srch_type = srch_type; }

    public String getSrch_keyword() { return srch_keyword; }

    public void setSrch_keyword(String srch_keyword) { this.srch_keyword = srch_keyword; }

    public String getMsgid() { return msgid; }

    public void setMsgid(String msgid) { this.msgid = msgid; }

    public String getErrcode() { return errcode; }

    public void setErrcode(String errcode) { this.errcode = errcode; }

    public String getErr_exp() { return err_exp; }

    public void setErr_exp(String err_exp) { this.err_exp = err_exp; }

    public String getField1() { return field1; }

    public void setField1(String field1) { this.field1 = field1; }
}
